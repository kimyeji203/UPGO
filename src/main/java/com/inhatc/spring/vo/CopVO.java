package com.inhatc.spring.vo;

public class CopVO {
	private int cop_no;
	private String cop_name;
	private String cop_content;
	private String cop_call;
	private String cop_addr;
	
	
	public int getCop_no() {
		return cop_no;
	}
	public void setCop_no(int cop_no) {
		this.cop_no = cop_no;
	}
	public String getCop_name() {
		return cop_name;
	}
	public void setCop_name(String cop_name) {
		this.cop_name = cop_name;
	}
	public String getCop_content() {
		return cop_content;
	}
	public void setCop_content(String cop_content) {
		this.cop_content = cop_content;
	}
	public String getCop_call() {
		return cop_call;
	}
	public void setCop_call(String cop_call) {
		this.cop_call = cop_call;
	}
	public String getCop_addr() {
		return cop_addr;
	}
	public void setCop_addr(String cop_addr) {
		this.cop_addr = cop_addr;
	}
	
	
}
